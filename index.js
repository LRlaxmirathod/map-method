//using map method double the array

let arr = [1,2,3,4,5,6,7]
arr.map((val)=>{
    console.log(val *2 )
})

//using map method triple the array

let array = [1,2,3,4,5,6,7,8]
array.map((val)=>{
    console.log(val * 3)
})


//using map method print binary number

let arra = [1,2,3,4,5,7,8]
arra.map((val)=>{
    console.log(val.toString(2))
})
